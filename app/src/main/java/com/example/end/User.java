package com.example.end;

public class User {
    String age;
    String gender;
    String interests;
    String viddeyatelnosti;
    String pitomci;

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getInterests() {
        return interests;
    }

    public void setInterests(String interests) {
        this.interests = interests;
    }

    public String getViddeyatelnosti() {
        return viddeyatelnosti;
    }

    public void setViddeyatelnosti(String viddeyatelnosti) {
        this.viddeyatelnosti = viddeyatelnosti;
    }

    public String getPitomci() {
        return pitomci;
    }

    public void setPitomci(String pitomci) {
        this.pitomci = pitomci;
    }
}
